#include <stdio.h>

char	*ft_strcpy(char *s1, char *s2)
{
	while (*s2 != '\0')
		*s1++ = *s2++;
	*s1 = '\0';
	return (s1);
}

int	main(int ac, char **av)
{
	if (ac == 3)
	ft_strcpy(av[2], av[1]);
	printf("%s", av[2]);
	return (0);
}
