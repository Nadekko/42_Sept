#include <unistd.h>

int	main(int ac, char **av)
{
	char	*temp;

	if (ac == 3)
	{
		temp = av[1];
		while (*temp && *av[2])
		{
			while (*temp == *av[2] && (*temp == *av[2]))
			{
				temp++;
				av[2]++;
			}
			av[2]++;
		}
		if (!(*temp))
			write(1, av[1], 1);
		av[1]++;
	}
	write(1, "\n", 1);
	return (0);
}
